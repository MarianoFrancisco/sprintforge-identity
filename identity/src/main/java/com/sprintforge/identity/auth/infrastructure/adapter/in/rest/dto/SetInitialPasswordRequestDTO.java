package com.sprintforge.identity.auth.infrastructure.adapter.in.rest.dto;

import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Pattern;
import jakarta.validation.constraints.Size;

import java.util.UUID;

public record SetInitialPasswordRequestDTO(
        UUID userId,
        @NotBlank(message = "La nueva contraseña es obligatoria")
        @Size(min = 8, max = 255, message = "La contraseña debe tener entre 8 y 255 caracteres")
        @Pattern(
                regexp = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@$!%*?&#._-])[A-Za-z\\d@$!%*?&#._-]+$",
                message = "La contraseña debe contener mayúsculas, minúsculas, números y un carácter especial"
        )
        String newPassword
) {
}
